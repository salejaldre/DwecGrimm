class Wesen{
    nombre;
    imagen;
    tipo;
    peligrosidad;
    aspecto;
    notas;

    constructor(nombre, imagen, tipo, peligrosidad, aspecto, notas){
        this.nombre = nombre;
        this.imagen = imagen;
        this.tipo = tipo;
        this.peligrosidad = peligrosidad;
        this.aspecto = aspecto;
        this.notas = notas;
    }
}